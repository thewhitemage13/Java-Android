package com.example.hw_1;

public class ChatItem {
    String avatar;
    String title;
    String message;
    String time;
    String count;

    public ChatItem(String avatar, String title, String message, String time, String count) {
        this.avatar = avatar;
        this.title = title;
        this.message = message;
        this.time = time;
        this.count = count;
    }
}
